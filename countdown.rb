def countdown(int)
  count = 0
  while int >= count
  puts "#{int} SECOND(S)!
  int -= 1
  end
  "HAPPY NEW YEAR!"
end

countdown(10)

def countdown_with_sleep(int)
  count = 0
  while sleep(int) >= count 
  puts "sleep(int) SECOND(S)!"
  sleep(int) -= 1
  end
  "HAPPY NEW YEAR"
end

countdown_with_sleep(7)




